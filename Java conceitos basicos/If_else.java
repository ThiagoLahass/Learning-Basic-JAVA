
public class If_else {

    public static void main(String[] args){

        int a = 20;
        int b = 10;

        // Se a condição for verdadeira ( a for igual a b )
        if( a == b ){
            System.out.println("A variável 'a' é igual a variável 'b'");
        }
        // Senão
        else{
            System.out.println("A variável 'a' é diferente da variável 'b'");
        }

    }
}
