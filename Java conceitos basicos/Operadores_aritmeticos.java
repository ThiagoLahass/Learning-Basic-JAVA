
public class Operadores_aritmeticos {

    public static void main(String[] args){

        /*
         * + Operador de Soma
         * - Operador de Subtração
         * / Operador de Divisão
         * * Operador de Multiplicação
         * % Operador de Módulo - Resto da divisão
         * ++ Incremento
         * -- Decremento
         */

        int a = 20;
        int b = 10;

        System.out.println(a + b);
        System.out.println(a - b);
        System.out.println(a / b);
        System.out.println(a * b);
        System.out.println(a % b);
        a++;
        System.out.println(a);
        a--;
        System.out.println(a);
        System.out.println(++a);
        System.out.println(--a);

    }
}
