
public class Operadores_relacionais {

    public static void main(String[] args){

        /*
         * == Operador de igualdade
         * >  Operador de Maior
         * <  Operador de Menor
         * >= Operador de Maior ou igual 
         * <= Operador de Menor ou igual
         * != Operador de Diferente
         */

        int a = 20;
        int b = 10;

        System.out.println(a == b);
        System.out.println(a > b);
        System.out.println(a < b);
        System.out.println(a >= b);
        System.out.println(a <= b);
        System.out.println(a != b);

    }
}
